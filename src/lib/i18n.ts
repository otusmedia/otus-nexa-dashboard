function mergeFromDictionary(): Record<string, string> {
  const out: Record<string, string> = {};
  for (const key of Object.keys(dictionary.en) as Array<keyof typeof dictionary.en>) {
    out[dictionary.en[key]] = dictionary["pt-BR"][key];
  }
  return out;
}

